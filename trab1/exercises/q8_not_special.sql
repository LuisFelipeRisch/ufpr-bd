SELECT COUNT(*)
FROM CUSTOMER
WHERE C_CUSTKEY IN 
(
    SELECT C_CUSTKEY
    FROM CUSTOMER 
    WHERE C_CUSTKEY IN
    (
        SELECT DISTINCT(O_CUSTKEY)
        FROM ORDERS 
    )
    EXCEPT
    SELECT C_CUSTKEY
    FROM CUSTOMER 
    WHERE C_CUSTKEY IN
    (
        SELECT DISTINCT(O_CUSTKEY)
        FROM ORDERS 
        WHERE O_COMMENT LIKE '%special request%'
    )
);